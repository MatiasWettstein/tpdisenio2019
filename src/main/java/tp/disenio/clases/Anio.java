package tp.disenio.clases;

public class Anio {
	//---------- Atributos
	private int id_anio;
	private double sumaAsegurada;
	private int inicioFabricacion;
	private int finFabricacion;


	public int getId_anio() {
		return id_anio;
	}
	public void setId_anio(int id_anio) {
		this.id_anio = id_anio;
	}
	public double getSumaAsegurada() {
		return sumaAsegurada;
	}
	public void setSumaAsegurada(double sumaAsegurada) {
		this.sumaAsegurada = sumaAsegurada;
	}
	public int getInicioFabricacion() {
		return inicioFabricacion;
	}
	public void setInicioFabricacion(int inicioFabricacion) {
		this.inicioFabricacion = inicioFabricacion;
	}
	public int getFinFabricacion() {
		return finFabricacion;
	}
	public void setFinFabricacion(int finFabricacion) {
		this.finFabricacion = finFabricacion;
	}


}

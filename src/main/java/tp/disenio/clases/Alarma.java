package tp.disenio.clases;

public class Alarma {
	
	private int id_alarma; 
	private float porcentaje;
	private boolean posee;
	private int id_usuario;
	public int getId_alarma() {
		return id_alarma;
	}
	public void setId_alarma(int id_alarma) {
		this.id_alarma = id_alarma;
	}
	public float getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(float porcentaje) {
		this.porcentaje = porcentaje;
	}
	public boolean isPosee() {
		return posee;
	}
	public void setPosee(boolean posee) {
		this.posee = posee;
	}
	public int getId_usuario() {
		return id_usuario;
	}
	public void setId_usuario(int id_usuario) {
		this.id_usuario = id_usuario;
	} 
	
	

}
